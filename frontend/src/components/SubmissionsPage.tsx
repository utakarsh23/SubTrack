'use client';

import { useState, useEffect } from 'react';
import { Submission } from '@/types/submission';
import { apiClient } from '@/lib/api';
import LeetCodeSubmissionTable from '@/components/LeetCodeSubmissionTable';

export default function SubmissionsPage() {
  const [submissions, setSubmissions] = useState<Submission[]>([]);
  const [loading, setLoading] = useState(true);
  const [error, setError] = useState<string | null>(null);

  useEffect(() => {
    fetchSubmissions();
  }, []);

  const fetchSubmissions = async () => {
    try {
      setLoading(true);
      const response = await apiClient.getAllSubmissions(100);
      setSubmissions(response.submissions);
    } catch (err) {
      setError('Failed to fetch submissions');
      console.error('Error fetching submissions:', err);
    } finally {
      setLoading(false);
    }
  };

  const handleSubmissionUpdate = (updatedSubmission: Submission) => {
    setSubmissions(prev => 
      prev.map(sub => 
        sub._id === updatedSubmission._id ? updatedSubmission : sub
      )
    );
  };

  if (loading) {
    return (
      <div className="min-h-screen bg-gray-900 flex items-center justify-center">
        <div className="text-center">
          <div className="w-12 h-12 border-4 border-blue-500/20 border-t-blue-500 rounded-full animate-spin mx-auto mb-4" />
          <p className="text-gray-400">Loading submissions...</p>
        </div>
      </div>
    );
  }

  if (error) {
    return (
      <div className="min-h-screen bg-gray-900 flex items-center justify-center">
        <div className="text-center">
          <div className="text-red-400 text-lg mb-4">{error}</div>
          <button
            onClick={fetchSubmissions}
            className="px-4 py-2 bg-blue-600 hover:bg-blue-700 text-white rounded-lg transition-colors"
          >
            Retry
          </button>
        </div>
      </div>
    );
  }

  return (
    <LeetCodeSubmissionTable 
      submissions={submissions} 
      onSubmissionUpdate={handleSubmissionUpdate}
    />
  );
}